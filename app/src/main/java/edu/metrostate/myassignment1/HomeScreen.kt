@Composable
fun HomeScreen() : Boolean{
    var login by remember { mutableStateOf(false) }
    var register by remember { mutableStateOf(false) }
    Column(
        modifier = Modifier
            .fillMaxSize()
            .background(Color.LightGray),
        verticalArrangement = Arrangement.Center,
        horizontalAlignment = Alignment.CenterHorizontally,
    ) {
        Text(
            text = "Todo App",
            fontSize = 48.sp,
            fontWeight = FontWeight.Bold,
        )

        Button(onClick = {
            login = true
            register = false
        }) {
            Text(
                text = "Login",
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold
            )
        }

        OutlinedButton(onClick = {
            register = true
            login = false
        }) {
            Text(
                text = "Create Account",
                fontSize = 24.sp,
                fontWeight = FontWeight.Bold
            )
        }
    }
    if(login && !register){
        return login
    }
    else
        return register
}

